export const getEmojisForLetter = (
  letter: string,
  maxNumberOfEmojis: number
): Emoji[] => {
  if (combinations[letter]) {
    const emojis = combinations[letter];
    return emojis.sort(() => 0.5 - Math.random()).slice(0, maxNumberOfEmojis);
  }

  return [];
};
